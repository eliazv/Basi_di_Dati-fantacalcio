package model;

public class Prestazione {
	private String idcalciatore;
	private String idmatch;
	private int puntiValutazione;
	
	public String getIdcalciatore() {
		return idcalciatore;
	}
	public void setIdcalciatore(String idcalciatore) {
		this.idcalciatore = idcalciatore;
	}
	public String getIdmatch() {
		return idmatch;
	}
	public void setIdmatch(String idmatch) {
		this.idmatch = idmatch;
	}
	public int getPuntiValutazione() {
		return puntiValutazione;
	}
	public void setPuntiValutazione(int puntiValutazione) {
		this.puntiValutazione = puntiValutazione;
	}
}
